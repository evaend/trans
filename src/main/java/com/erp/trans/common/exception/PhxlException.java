package com.erp.trans.common.exception;

/**
 * 基本异常类定义 
 * */
public class PhxlException extends Exception{

	public PhxlException() {
		super();
	}

	public PhxlException(String message, Throwable cause) {
		super(message, cause);
	}

	public PhxlException(String message) {
		super(message);
	}

	public PhxlException(Throwable cause) {
		super(cause);
	}
	
	
	
}
